public class VarType {
    public static void main(String args[]){
        var vIntegerNumber = 10;
        System.out.println("vIntegerNumber = " + vIntegerNumber);
        
        var vDoubleNumber = 10.0;
        System.out.println("vDoubleNumber = " + vDoubleNumber);
        
        var vFloatNumber = 10.0F;
        System.out.println("vFloatNumber = " + vFloatNumber);
    }
}
