public class SpecialCharacters {
    public static void main(String args[]){
        String sName = "Karla";
        
        System.out.println("New line: \n" + sName); //New line character
        System.out.println("Tab: \t" + sName);      //Tab
        System.out.println("Backspace: \b" + sName); //Backspace
        System.out.println("Simple colon: \'" + sName + "\'");
        System.out.println("Double colon: \"" + sName + "\"");
                
    }
}
